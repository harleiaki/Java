package br.com.impacta.reais;

public class VariaveisReais {
	public static void main(String[] args) {
		
		float f1 = (float) 100.1;  //f ou F 
		double d1 = 100.1;
		
		float f2 = 100;
		double d2 = 100;
		
		System.out.println("Valor float: " + f1);
		System.out.println("Valor double: " + d1);
		System.out.println("Valor float: " + f2);
		System.out.println("Valor double: " + d2);
		
	}

}
